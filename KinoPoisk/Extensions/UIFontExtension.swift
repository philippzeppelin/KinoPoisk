import UIKit

extension UIFont {
    // Arial Bold
    static func arialBold15() -> UIFont? {
        return UIFont.init(name: "Arial Bold", size: 15)
    }

    static func arialBold25() -> UIFont? {
        return UIFont.init(name: "Arial Bold", size: 25)
    }

    // Arial
    static func arial13() -> UIFont? {
        return UIFont.init(name: "Arial", size: 13)
    }
}
